#include <stdio.h>
int main()

{

   int a, b, c, d, m, n, o;

   printf("Enter two integers\n");

   scanf("%d%d", &a, &b);

   c = a + b;

   d = a - b;

   m = a * b;

   n = a / b;  

   o = a % b; 

   printf("Sum = %d\n", c);

   printf("Difference = %d\n", d);

   printf("Multiplication = %d\n", m);

   printf("Division = %d\n", n);

   printf("Modulo = %d\n", o);

   return 0;

}
